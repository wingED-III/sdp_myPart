public class UniversityItem {
    private String mUnversityName;
    private int mUniversityImage;

    public UniversityItem(String UnversityName,int UniversityImage) {
        this.mUnversityName = UnversityName;
        this.mUniversityImage = UniversityImage;
    }

    public String getmUnversityName()
    {
        return mUnversityName;
    }
    public int getmUniversityImage()
    {
        return mUniversityImage;
    }

}
